def city_country(city, country= 'United States'):
    """Takes the name of a city and country. """
    return(f"{city}, {country}")

place_0 = city_country('Paris', 'France')
print(place_0)
place_1 = city_country('New York')
print(place_1)
place_2 = city_country('Shenzheng', 'China')
print(place_2)

